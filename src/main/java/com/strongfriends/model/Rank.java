package com.strongfriends.model;

public class Rank {
    private int id ;
    private String name;
    private String gender;
    private float weight;
    private float squat;
    private float bench;
    private float deadlift;
    private float total;
    private float wilks;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public float getSquat() {
        return squat;
    }

    public void setSquat(float squat) {
        this.squat = squat;
    }

    public float getBench() {
        return bench;
    }

    public void setBench(float bench) {
        this.bench = bench;
    }

    public float getDeadlift() {
        return deadlift;
    }

    public void setDeadlift(float deadlift) {
        this.deadlift = deadlift;
    }

    public float getTotal() {
        return total;
    }

    public void setTotal(float total) {
        this.total = total;
    }

    public float getWilks() {
        return wilks;
    }

    public void setWilks(float wilks) {
        this.wilks = wilks;
    }



}
